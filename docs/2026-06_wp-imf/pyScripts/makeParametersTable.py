"""
Working paper: generate Table 1 (tab:parameters), "Selected Model Parameters".

Writes only the `tabular` environment to ../parametersTable.tex; the surrounding
table float, caption, label, source and note stay in draftPaper.tex (same split
as multipliersTable.tex).

HYBRID data sourcing. Most rows of Table 1 are *curated* calibration values that
deliberately differ from the raw model parameters and so stay encoded here (see
"why curated" notes per row below). Four parameters map one-for-one onto the
solved model and are read live from the committed _results.mat (AE =
Model_HumanCapital_exp_gc, EMDE = EM_Model_HumanCapital_exp_gc), so a
recalibration of any of them flows into the table automatically:

    alpha_G <- alphaG      (output elasticity, infrastructure)
    rho_A   <- rho_ZZRD    (AR(1) coefficient, technology stock)
    alpha_HA <- alphaHA    (HC loading in tech creation; EMDE "--", channel off)
    alpha_RD <- alphaRD    (R&D loading in tech creation; EMDE "--", channel off)
    varsigma <- rhoSADOPT  (adoption-probability elasticity, AE; EMDE "--", dormant)
    phi     <- phiob       (survival rate of adopted technologies, 1 - 0.08/4)
    e_GI    <- eGI_ss      (inefficiency, infrastructure investment)
    e_GE    <- eGE_ss      (inefficiency, human capital investment)
    e_GRD   <- eGRD_ss     (inefficiency, public R&D; EMDE shown "--", channel off)
    delta   <- delta/deltaH (depreciation, public capital; the two are equal)
    mu      <- alphaH      (human capital elasticity, public stock)
    gamma   <- muy         (human capital elasticity, time input)

alpha_HA and alpha_RD are shown as their EXPLICIT model values (0.10 and 0.0189),
i.e. the per-quarter loadings in the AR(1) law of motion for created technology
(model_block.modpart:53). They are NOT normalized to the implied long-run
elasticities alpha/(1-rho_A) ~ 0.45 and 0.10 here; the table NOTE in draftPaper.tex
explains that long-run interpretation instead. (alphaRD is coded as
0.09*(1-rho_ZZRD)=0.0189; alphaHA is stored directly as 0.10.)

The efficiency gaps e_GI/e_GE/e_GRD equal Table 2's data-derived medians (the AE
median for advanced economies, the (EM+LIC)/2 average for EMDEs), so reading them
here keeps Table 1 and Table 2 consistent (this also corrected a stale EMDE e_GI
of 0.42 -> 0.41 and the AE e_GRD of 0.41 -> 0.40, recalibrated to the 0.399 data
median). The EMDE e_GRD is reported "--" because the R&D channel is shut down there
(alphaRD=0), even though the model still carries eGRD_ss=0.2.

varsigma (adoption-probability elasticity) reads the AE rhoSADOPT and is "--" for
EMDEs: their endogenous innovation channel is off, so the spending-driven adoption
response is dormant (verified: the adoption block does not move in EMDE
experiments), exactly as for alpha_HA/alpha_RD.

The paper's adoption-probability SCALE q_0 is intentionally NOT a row here: it is
not a model parameter. The code parameterizes the scale as `kappaprob` on RAW
(non-detrended) spending, and kappaprob is SOLVED in steady state to hit the
targeted adoption probability probadoptss=0.2/4=0.05 (kappaprob = 0.05/S_ss^rhoSADOPT
= 0.309 AE / 0.084 EMDE). Since q_0 corresponds to no stored parameter, it is
described in the calibration text (draftPaper.tex) rather than tabulated.

Every row in this table is now read from _results.mat or is a documented "--".

Writes (\\input by draftPaper.tex):
  ../parametersTable.tex
"""
from pathlib import Path

from scipy.io import loadmat

HERE = Path(__file__).resolve().parent
OUT = HERE.parent / "parametersTable.tex"          # docs/2026-06_wp-imf/
REPO = HERE.parents[2]                             # repo root
MODELS = REPO / "models"
AE_MODEL = "Model_HumanCapital_exp_gc"
EM_MODEL = "EM_Model_HumanCapital_exp_gc"

HEADER = ("% Generated by pyScripts/2026-06_wp-imf/makeParametersTable.py "
          "(run via the docs copy). Do not edit by hand.")


def read_params(model):
    """Return {param_name: value} from a committed <model>_results.mat."""
    m = loadmat(MODELS / model / "Output" / f"{model}_results.mat")
    M = m["M_"]
    names = [str(x[0]) for x in M["param_names"][0, 0][:, 0]]
    vals = M["params"][0, 0].ravel()
    return dict(zip(names, vals))


def fmt(x, dp):
    return f"{float(x):.{dp}f}"


def build_parameters():
    """(symbol, description, AE value, EMDE value); "--" where a channel is off.

    The four clean parameters are read from the AE/EMDE _results.mat; the rest are
    curated literals (see module docstring for why each is not read from the mat)."""
    ae = read_params(AE_MODEL)
    em = read_params(EM_MODEL)

    # delta and deltaH are the same depreciation rate; verify before showing once.
    assert abs(ae["delta"] - ae["deltaH"]) < 1e-12, "delta != deltaH (AE)"
    assert abs(em["delta"] - em["deltaH"]) < 1e-12, "delta != deltaH (EMDE)"

    return [
        # symbol                       description                                AE                            EMDE
        (r"$\alpha_G$",              "Output elasticity, infrastructure",       fmt(ae["alphaG"], 3),         fmt(em["alphaG"], 2)),      # READ: alphaG (AE 0.054, EMDE 0.17)
        (r"$\alpha_{HA}$",           "Human capital loading, innovation",       fmt(ae["alphaHA"], 2),        "--"),      # READ AE: alphaHA (per-quarter loading); EMDE "--" (channel off)
        (r"$\alpha_{RD}$",           r"Public R\&D loading, innovation",        fmt(ae["alphaRD"], 4),        "--"),      # READ AE: alphaRD = 0.09*(1-rho_A); EMDE "--" (channel off)
        (r"$\rho_A$",                "AR(1) coefficient, technology stock",     fmt(ae["rho_ZZRD"], 2),       fmt(em["rho_ZZRD"], 2)),   # READ: rho_ZZRD
        (r"$\phi$",                  "Survival rate of adopted technologies",   fmt(ae["phiob"], 2),          fmt(em["phiob"], 2)),      # READ: phiob = 1 - 0.08/4 (8%/yr obsolescence)
        (r"$e^{GI}$",                "Inefficiency, infrastructure investment", fmt(ae["eGI_ss"], 2),         fmt(em["eGI_ss"], 2)),     # READ: eGI_ss
        (r"$e^{GE}$",                "Inefficiency, human capital investment",  fmt(ae["eGE_ss"], 2),         fmt(em["eGE_ss"], 2)),     # READ: eGE_ss
        (r"$e^{GRD}$",               r"Inefficiency, public R\&D spending",     fmt(ae["eGRD_ss"], 2),        "--"),      # READ AE: eGRD_ss; EMDE "--" (R&D channel off)
        (r"$\varsigma$",             "Elasticity, adoption probability",        fmt(ae["rhoSADOPT"], 2),      "--"),      # READ AE: rhoSADOPT; EMDE "--" (adoption response dormant, channel off)
        (r"$\delta^{GI},\delta^{GE}$", "Depreciation, public capital",          fmt(ae["delta"], 3),          fmt(em["delta"], 3)),      # READ: delta (= deltaH)
        (r"$\mu$",                   "Human capital elasticity, public stock",  fmt(ae["alphaH"], 2),         fmt(em["alphaH"], 2)),     # READ: alphaH
        (r"$\gamma$",                "Human capital elasticity, time input",    fmt(ae["muy"], 2),            fmt(em["muy"], 2)),        # READ: muy
    ]


def main():
    parameters = build_parameters()
    lines = [
        HEADER,
        r"\begin{tabular}{llcc}",
        r"    \toprule",
        r"    & Description & AE & EMDE \\",
        r"    \midrule",
    ]
    for sym, desc, ae, emde in parameters:
        lines.append(f"    {sym} & {desc} & {ae} & {emde} \\\\")
    lines += [r"    \bottomrule", r"\end{tabular}"]
    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"  Wrote {OUT.name}")


if __name__ == "__main__":
    main()
